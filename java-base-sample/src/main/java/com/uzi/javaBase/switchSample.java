public class switchSample {

    public static void main(String[] args) {
        int i = 0;
        switch (i){
            case 1:
                System.out.println("this is one");
                break;
            case 2:
                System.out.println("this is two");
                break;
            case 3:
                System.out.println("this is three");
                break;
            default:
                System.out.println("I don't know");
                break;
        }

    }

}
